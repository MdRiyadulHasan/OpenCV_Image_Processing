import cv2
img=cv2.imread("images\messi2.jpg",1)
img1=cv2.resize(img,(300,300))
img2=cv2.resize(img,(0,0), fx=.5, fy=.5)
img3=cv2.resize(img,(0,0), fx=1.25, fy=1.25)
cv2.imshow('original_image', img)
cv2.imshow('resize_by_pixel',img1)
cv2.imshow('resize_by_ratio_small',img2)
cv2.imshow('resize_by_ratio_big',img3)
cv2.waitKey(10000)
cv2.destroyAllWindows()